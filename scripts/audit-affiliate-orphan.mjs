/**
 * READ-ONLY audit for an orphaned affiliate (downline not visible under upline).
 *
 * Reads only — never writes. Prints the two users' lineage fields, the downline's
 * client/balance/deposits, and any referral commissions, so we can confirm WHY
 * the downline is filtered out of the upline's direct list.
 *
 * The direct list on both the client (/dashboard referrals) and admin
 * (/admin/referral-tree) is built with `where: { referredById: <upline.id> }`.
 * So a downline appears under an upline IFF downline.referredById === upline.id.
 * If it's null/wrong, the row is orphaned regardless of ledger balance.
 *
 * Usage (from an env that has the production DATABASE_URL):
 *   DOWNLINE=asilehsarem@gmail.com UPLINE=alejandro152@gmail.com \
 *     node scripts/audit-affiliate-orphan.mjs
 */
import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();
const DOWNLINE = (process.env.DOWNLINE || "asilehsarem@gmail.com").toLowerCase();
const UPLINE = (process.env.UPLINE || "alejandro152@gmail.com").toLowerCase();

// NEVER select passwordHash — read only the columns relevant to genealogy.
const LINEAGE_SELECT = {
  id: true,
  email: true,
  username: true,
  name: true,
  referralCode: true,
  referredById: true,
  referralPath: true,
  referralDepth: true,
  rootSponsorId: true,
  clientId: true,
  createdAt: true,
};

function short(id) {
  return id ? `${id.slice(0, 8)}…` : "(null)";
}

async function main() {
  console.log("=== READ-ONLY AFFILIATE ORPHAN AUDIT ===\n");

  // 1) Both users + lineage fields (the real schema: model User -> table "User")
  const [down, up] = await Promise.all([
    prisma.user.findFirst({ where: { email: { equals: DOWNLINE, mode: "insensitive" } }, select: LINEAGE_SELECT }),
    prisma.user.findFirst({ where: { email: { equals: UPLINE, mode: "insensitive" } }, select: LINEAGE_SELECT }),
  ]);

  console.log("1) USERS (password masked, never queried)\n");
  for (const [label, u] of [["DOWNLINE (Elisa)", down], ["UPLINE (Alejandro)", up]]) {
    if (!u) { console.log(`  ${label}: NOT FOUND`); continue; }
    console.log(`  ${label}`);
    console.log(`    id            = ${u.id}`);
    console.log(`    email         = ${u.email}`);
    console.log(`    username      = ${u.username ?? "(null)"}`);
    console.log(`    referralCode  = ${u.referralCode ?? "(null)"}`);
    console.log(`    referredById  = ${u.referredById ?? "(null)"}  ${u.referredById ? "" : "<-- ORPHAN if this should point to upline"}`);
    console.log(`    referralPath  = ${u.referralPath ?? "(null)"}`);
    console.log(`    referralDepth = ${u.referralDepth}`);
    console.log(`    rootSponsorId = ${u.rootSponsorId ?? "(null)"}`);
    console.log(`    clientId      = ${u.clientId ?? "(null)"}`);
    console.log(`    createdAt     = ${u.createdAt.toISOString()}\n`);
  }

  if (!down || !up) { console.log("Cannot continue — one user is missing."); return; }

  // 2) The exact orphan verdict
  const linked = down.referredById === up.id;
  console.log("2) DIRECT-LIST VERDICT");
  console.log(`   downline.referredById (${short(down.referredById)}) === upline.id (${short(up.id)})?  ${linked ? "YES — visible" : "NO — ORPHANED"}\n`);

  // 3) Downline client + balance + deposits (ledger is independent of referral link)
  if (down.clientId) {
    const [client, deposits] = await Promise.all([
      prisma.client.findUnique({
        where: { id: down.clientId },
        select: { id: true, accountNumber: true, status: true, initialDeposit: true, startDate: true },
      }),
      prisma.transaction.findMany({
        where: { clientId: down.clientId, type: "DEPOSIT" },
        orderBy: { createdAt: "desc" },
        take: 5,
        select: { id: true, amount: true, status: true, method: true, createdAt: true },
      }),
    ]);
    console.log("3) DOWNLINE LEDGER (proves she is a real funded client despite the orphan)");
    console.log(`   client: ${client?.accountNumber} status=${client?.status} initialDeposit=${client?.initialDeposit}`);
    const approved = deposits.filter((d) => d.status === "APPROVED").reduce((s, d) => s + d.amount, 0);
    console.log(`   approved deposits total = ${approved}`);
    for (const d of deposits) console.log(`     - ${d.createdAt.toISOString()}  ${d.status}  $${d.amount}  ${d.method ?? ""}`);
    console.log();
  }

  // 4) Referral commissions — did the upline ever get paid for this downline?
  const [l1ForDown, l1ToUp] = await Promise.all([
    prisma.referralCommission.findMany({ where: { referredUserId: down.id }, select: { id: true, referrerId: true, status: true, commission: true, createdAt: true } }).catch(() => []),
    prisma.referralCommission.count({ where: { referrerId: up.id } }).catch(() => 0),
  ]);
  console.log("4) REFERRAL COMMISSIONS");
  console.log(`   commissions generated BY downline's activation: ${l1ForDown.length}`);
  for (const c of l1ForDown) console.log(`     - to referrer ${short(c.referrerId)}  ${c.status}  $${c.commission}  ${c.createdAt.toISOString()}`);
  console.log(`   total commission rows earned by upline (any downline): ${l1ToUp}\n`);

  // 5) PROPOSED FIX (printed only — NOT executed)
  const path = up.referralPath ? `${up.referralPath}/${up.id}` : up.id;
  const depth = (up.referralDepth ?? 0) + 1;
  const root = up.rootSponsorId ?? up.id;
  console.log("5) PROPOSED SINGLE-USER FIX (dry-run — nothing written)");
  console.log("   Setting downline.referredById alone is NOT enough — the materialized");
  console.log("   path fields must match, or the genealogy tree / L2 compression break.");
  console.log("   Correct values derived from the upline's lineage:");
  console.log(`     referredById  = ${up.id}`);
  console.log(`     referralPath  = ${path}`);
  console.log(`     referralDepth = ${depth}`);
  console.log(`     rootSponsorId = ${root}`);
  console.log("   (The committed fix script applies exactly these via a single update,");
  console.log("    then recomputes any of the downline's OWN descendants via backfillGenealogy.)");
}

main()
  .catch((e) => { console.error("AUDIT ERROR:", e?.message ?? e); process.exitCode = 1; })
  .finally(() => prisma.$disconnect());
